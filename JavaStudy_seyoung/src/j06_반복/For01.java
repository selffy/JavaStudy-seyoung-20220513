package j06_반복;

public class For01 {

	public static void main(String[] args) {
		
		for(int i = 0; i < 5; i++) {
			System.out.println(i + 1);

		}
		int i = 0;
		while(i < 5) {
				System.out.println(i + 1);
				i++;
		}
		
		int num = 10;
		
		for( i = 0; i < num; i++) {
			System.out.println(num - i);
		}
	}
}
		
