package j06_반복;

public class While01 {

	public static void main(String[] args) {
		
		int i = 0;
		int result = 0;
		
		while( i < 100) {
			result = result + (i + 1);
			i++; // 조건 변화 수식
			System.out.println(result);
	
		/*
		 * 반복에서는 보통 i++(후증가)를 많이 사용한다.
		 * 컴퓨터는 항상 0부터 시작한다.
		 */
			
			
		}
	
	}
	
}
