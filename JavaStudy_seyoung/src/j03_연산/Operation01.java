package j03_연산;

public class Operation01 {

	public static void main(String[] args) {
		
		int num = 10;
		
		num = num + 1;	
		
		System.out.println(++num);
		// 선증가 ++num
		
		// 후증가 num++ : 그 다음부터 증가
	
		System.out.println(--num);
		System.out.println(num--);
		
	
		
		/* 관계연산자
		 * : > 왼쪽 항이 큼
		 * : < 왼쪽 항이 작음
		 * : >= 왼쪽 항이 오른쪽 항보다 크거나 같음
		 * : <= 왼쪽 항이 오른쪽 항보다 작음
		 * : == 두 항이 같으면 참
		 * : != 두 항이 다르면 참 
		 * 
		 */ 
		
		

	}
}
